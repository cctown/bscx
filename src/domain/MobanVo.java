package domain;

public class MobanVo {
	private String name;
	private String dsc;
	private String id;
	
	public String getName(){
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDsc(){
		return this.dsc;
	}
	public void setDsc(String dsc) {
		this.dsc = dsc;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
